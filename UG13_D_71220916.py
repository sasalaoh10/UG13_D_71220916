# ## Y
import turtle
t = turtle.Turtle()
s = turtle.Screen()

a = 1
for i in range(a):
    t.pensize(30)
    t.pencolor("red")
    t.left(115)
    t.forward(150)
    t.backward(150)
    t.right(60)
    t.forward(150)
    t.backward(150)
    t.right(145)
    t.forward(150)
    t.penup()
    t.backward(150)
    t.left(90)
    t.pendown()

t.up()

t.forward(100)

t.down()


